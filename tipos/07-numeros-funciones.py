import math

print(round(1.3))  # aproximar
print(round(1.7))
print(round(1.5))
print(abs(-2))  # valor absoluto

print(math.ceil(1.1))  # aproxima hacia arriba
print(math.floor(1.99999))  # aproxima hacia abajo
print(math.isnan(23))  # si corresponde a que no es un numero
# print(math.isnan("23"))

print(math.pow(10, 3))  # elevar, base y potencia
print(math.sqrt(9))  # raiz cuadrada

